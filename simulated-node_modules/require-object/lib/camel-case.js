'use strict';

/* Converts tokens into camelCase */

const path = require('path');
const tokenize = require(path.resolve(__dirname, 'tokenize'));

module.exports = (tokens) => {

	if (!(tokens instanceof Array)) throw new Error('camel-case.js: Argument must be an Array.');

	tokens = tokens.map((token, i) => {
		if (i === 0) return token;

		let chars = token.split('');

		chars[0] = chars[0].toUpperCase();

		return chars.join('');
	});

	return tokens.join('');

}
